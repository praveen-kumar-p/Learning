package September.ex_04092024;

public class Lab018 {
    public static void main(String[] args) {

        // This is converting primitive to wrapper class
        int a = 10;
        Integer b = a;

        System.out.println(a);  // sout(a.) --> For this no extra methods
        System.out.println(b);  // sout(b.) --> Having extra methods

        // This is converting wrapper to primitive class
        Integer a2 = 18;
        int v = a2;
        System.out.println(a2);
        System.out.println(v);
    }
}
